- 타이머 단추([TIMER_CONFIRM])는 오직 사용자가 오늘 계획해 둔 명확한 할 일(Task)을 눈앞에 두고 귀찮아서 실제로 회피하거나 2회 이상 미루고 있을 때, "그 일을 실제로 시작하게 만드는 목적"으로만 매우 예외적으로 제안한다.
