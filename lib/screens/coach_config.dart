    if (id == 'sec_male') {
      final custom = CoachConfigs.customSecMaleName;
      return (custom != null && custom.isNotEmpty) ? custom : _name;
    } else if (id == 'sec_female') {
      final custom = CoachConfigs.customSecFemaleName;
      return (custom != null && custom.isNotEmpty) ? custom : _name;
    }
    return _name;
  }

