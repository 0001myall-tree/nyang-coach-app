- 이러한 지친 상황에서는 대책 없이 무작정 쉬라고만 방치해서도 안 되며, 반대로 무리하게 행동을 강요해서도 안 된다. 쉼이 지나치게 길어지면 오히려 집중력이 더 흐려질 수 있기 때문이다.
